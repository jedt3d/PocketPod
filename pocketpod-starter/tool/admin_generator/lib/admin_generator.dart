import 'package:yaml/yaml.dart';

class AdminModel {
  const AdminModel({
    required this.className,
    required this.fields,
    this.tableName,
  });

  final String className;
  final String? tableName;
  final List<AdminField> fields;

  String get variableName => _lowerFirst(className);
  String get title => _pluralize(_splitWords(className));
  String get routeName => '/admin/${_kebabCase(_pluralize(className))}';
}

class AdminField {
  const AdminField({required this.name, required this.rawType});

  final String name;
  final String rawType;

  String get dartType => rawType.replaceAll('?', '').trim();
  bool get isNullable => rawType.trim().endsWith('?');
  String get label => _splitWords(name);

  AdminFieldKind get kind {
    return switch (dartType) {
      'String' => AdminFieldKind.text,
      'int' => AdminFieldKind.integer,
      'double' => AdminFieldKind.decimal,
      'bool' => AdminFieldKind.boolean,
      'DateTime' => AdminFieldKind.dateTime,
      _ => AdminFieldKind.unsupported,
    };
  }
}

enum AdminFieldKind { text, integer, decimal, boolean, dateTime, unsupported }

class AdminGenerator {
  const AdminGenerator();

  AdminModel parseModel(String yamlSource) {
    final document = loadYaml(yamlSource);
    if (document is! YamlMap) {
      throw const FormatException('Model YAML must be a map.');
    }

    final className = document['class'];
    if (className is! String || className.trim().isEmpty) {
      throw const FormatException('Model YAML must contain a class name.');
    }

    final fieldsNode = document['fields'];
    if (fieldsNode is! YamlMap || fieldsNode.isEmpty) {
      throw const FormatException('Model YAML must contain fields.');
    }

    final fields = <AdminField>[];
    for (final entry in fieldsNode.entries) {
      final name = entry.key;
      final type = entry.value;
      if (name is! String || name.trim().isEmpty) {
        throw const FormatException('Field names must be strings.');
      }
      if (type is! String || type.trim().isEmpty) {
        throw FormatException('Field "$name" must have a scalar type.');
      }
      fields.add(AdminField(name: name, rawType: type));
    }

    final tableName = document['table'];
    return AdminModel(
      className: className.trim(),
      tableName: tableName is String ? tableName.trim() : null,
      fields: fields,
    );
  }

