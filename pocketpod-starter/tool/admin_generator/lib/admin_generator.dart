        AdminFieldKind.dateTime => '2026-06-30',
        AdminFieldKind.text => 'Sample ${_escapeHtml(field.label)}',
        AdminFieldKind.unsupported => _escapeHtml(field.rawType),
      };
      return '<td>$value</td>';
    }).join();
